#include "LinkedList.hpp"
#include <iostream>

int main(int argc, const char *argv[]) {
  LinkedList list;

  list.printList();

  for (int i = 0; i < 10; i++) {
    list.insertAtHead(i);
    list.printList();
  }

  list.printList();
    
  list.freeList();
    
  list.printList();

  return 0;
}

